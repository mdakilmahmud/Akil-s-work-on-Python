# Newline (\n) - moves text to a new line
print("Hello\nWorld")

# Tab (\t) - adds a horizontal space
print("Name:\tFaysal")
print("Age:\t20")

# Backslash (\\) - allows us to use a backslash in text
print("Path : C:\\")

# Double quotes (\"), Single quotes (\') - for printing quotes inside strings
print("She said, \"Python is fun!\"")
print('It\'s a wonderful day!')
