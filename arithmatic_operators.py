# Addition (+)
print("Addition:", 5 + 3)

# Subtraction (-)
print("Subtraction:", 10 - 7)

# Multiplication (*)
print("Multiplication:", 4 * 3)

# Division (/)
print("Division:", 8 / 2)  
# Floor Division (//) - rounds down to the nearest whole number
print("Floor Division:", 7 // 2) 

# Modulus (%) - gives the remainder of division
print("Modulus:", 7 % 2)  

# Exponentiation (**) - raises a number to a power
print("Exponentiation:", 3 ** 2) 
