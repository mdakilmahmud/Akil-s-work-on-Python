# Comparison and Boolean Combination
num1 = 15
num2 = 20

# Comparison Operations
print("num1 > num2:", num1 > num2)           
print("num1 < num2:", num1 < num2)          
print("num1 == num2:", num1 == num2)         
print("num1 != num2:", num1 != num2)       
print("num1 >= 10 and num2 <= 25:", num1 >= 10 and num2 <= 25) 
print("num1 < 10 or num2 > 15:", num1 < 10 or num2 > 15) 
